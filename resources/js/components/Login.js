function Login(props) {
    const classes = useStyles();
    const [email, setEmail] = React.useState('')
    const [password, setPassword] = React.useState('')

    const [open, setOpen] = React.useState(false);

    function handleClick() {
      console.log('SET OPEEEEEEEEEEEEEEEEEEEEEN');
      
      setOpen(true);
    }
  
    function handleClose(event, reason) {
      if (reason === 'clickaway') {
        return;
      }
  
      setOpen(false);
    }

    function onSubmit(e) {
        e.preventDefault()
        
        const user = {
            email: email,
            password: password,
        }
        axios.post('api/login', user, {
          headers: { 'Content-Type': 'application/json' }
        }).then(response => {
          console.log(response);
          localStorage.setItem('usertoken', response.data.token)
          localStorage.setItem('name', JSON.stringify(response.data.user.name))
          localStorage.setItem('id', JSON.stringify(response.data.user.id))
          localStorage.setItem('homestayId', JSON.stringify(response.data.homestayId))
          console.log(localStorage);
        })
        .catch(error => {
          if (error.response.status == 422) {
            alert('Field should not empty')
          } else {
            alert('Email or Passowrd not valid')
          }
          console.log(error.response);
          console.log("error");
          console.log(response);
          alert(error)
        })
        .then( () => {
          if (JSON.parse(localStorage.getItem('name')) != 'admin') {
            console.log('ifffffffffffffffffffffffffff');
            handleClick();
            props.history.push(`/ownerhomestay`);
          }
          else {
            console.log('elseeeeeeeeeeeeeeeeeeeeeeeeee');
            props.history.push(`/`);
          }
        })
    }

  return (
    <Container component="main" maxWidth="xs">
      <CssBaseline />
      <div className={classes.paper}>
        <Avatar className={classes.avatar}>
          <LockOutlinedIcon />
        </Avatar>
        <Typography component="h1" variant="h5">
          Sign in
        </Typography>
        <form className={classes.form}>
          <TextField
            value={email}
            onChange={event => setEmail(event.target.value)}
            variant="outlined"
            margin="normal"
            required
            fullWidth
            id="email"
            label="Email"
            name="email"
            autoComplete="email"
            autoFocus
          />
          <TextField
            value={password}
            onChange={event => setPassword(event.target.value)}
            variant="outlined"
            margin="normal"
            required
            fullWidth
            name="password"
            label="Password"
            type="password"
            id="password"
            autoComplete="current-password"
          />

          <Button
            type="submit"
            onClick={onSubmit}
            fullWidth
            variant="contained"
            color="primary"
            className={classes.submit}
          >
            Sign In
          </Button>
          <Grid container>
            <Grid item>
              <Link to="/register" variant="body2">
                {"Don't have an account? Sign Up"}
              </Link>
            </Grid>
          </Grid>
        </form>
      </div>
      <Snackbar
        anchorOrigin={{
          vertical: 'bottom',
          horizontal: 'left',
        }}
        open={open}
        autoHideDuration={6000}
        onClose={handleClose}
      >
        <MySnackbarContentWrapper
          onClose={handleClose}
          variant="success"
          message="This is a success message!"
        />
      </Snackbar>
    </Container>
  );
}

export default Login